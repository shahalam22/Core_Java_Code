public class IF_condition_05 {
    public static void main(String[] args) {
        int a = 5;
        if(a>0){
            System.out.println("It is a positive number.");
        } else if(a<0){
            System.out.println("It is a negative number.");
        } else {
            System.out.println("It is ZERO");
        }
    }
}
